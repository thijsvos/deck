pub fn render(text: &str) -> Vec<String> {
    let glyphs: Vec<[&str; 5]> = text.chars().filter_map(glyph).collect();

    if glyphs.is_empty() {
        return vec![text.to_uppercase()];
    }

    (0..5)
        .map(|row| {
            glyphs
                .iter()
                .map(|g| g[row])
                .collect::<Vec<_>>()
                .join(" ")
        })
        .collect()
}
