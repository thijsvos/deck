pub fn render(text: &str, style: FontStyle) -> Vec<String> {
    let glyph_fn = match style {
        FontStyle::Block => glyph_block,
        FontStyle::Thin => glyph_thin,
    };

    let glyphs: Vec<[&str; 5]> = text.chars().filter_map(glyph_fn).collect();

    if glyphs.is_empty() {
        return vec![text.to_uppercase()];
    }

    (0..5)
        .map(|row| {
            glyphs
                .iter()
                .map(|g| g[row])
                .collect::<Vec<_>>()
                .join(" ")
        })
        .collect()
}
